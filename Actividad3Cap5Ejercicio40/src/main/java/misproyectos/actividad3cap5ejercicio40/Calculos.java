package misproyectos.actividad3cap5ejercicio40;

public class Calculos {
    public static double raiz(double numero){
        double x = Math.sqrt(numero);
        return x;
    }
    
    public static double cuadrado(double numero){
        double x = Math.pow(numero,2);
        return x;
    }
    
    public static double cubo(double numero){
        double x = Math.pow(numero,3);
        return x;
    }
}

