    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        double pagoHora = Double.valueOf(txtSalariohora.getText());
        double Horas = Double.valueOf(txtHorasTrabajadas.getText());
        String Nombre = String.valueOf(txtNombre.getText());
        
        double salario = Salario.calcularSalario(pagoHora,Horas);
        
        txtNombre2.setText(String.valueOf(Nombre));
        
        if (salario > 450000){
            txtSalario.setText(String.valueOf(salario));
        }else{
            txtSalario.setText("Su salario es muy bajo");
        }
        
    }//GEN-LAST:event_btnRegistrarActionPerformed

